/*10. Write a program to swap two numbers without using third variable*/

#include <stdio.h>
int main()
{
    int a, b;
    printf("Enter the first Number 'a' :  ");
    scanf("%d", &a);
    printf("Enter the second Number 'b' :  ");
    scanf("%d", &b);
    printf("\nSwapping the Numbers\n");
    printf("First Number is %d\n", b);
    printf("Second Number is %d\n", a);
    return 0;
}
